package Operators;

public class ShortcutOperators {

	public static void main(String[] args) {
		int a=1;
		System.out.println(++a); //pre-increment
		a=1;
		System.out.println(a++); //post-increment
		a=1;
		System.out.println(a+=10);
		a=1;
		System.out.println(a-=10);
		a=1;
		System.out.println(a*=10);
		a=1;
		System.out.println(a/=10);

     }

}
